function [yps1,yps2] = ParamClass(mue1,mue2,sig1,sig2)
    
    yps1 = (sig1^2 * mue2 - sig2^2 * mue1 + sqrt(-1 * sig1^2 * sig2^2 *...
             (2* mue2 * mue1 - mue2^2 - 2* sig2^2 * log(sig2/sig1) ...
             - mue1^2 + 2* sig1^2*log(sig2/sig1)))) / (-1* sig2^2 + sig1^2);
         
    yps2 = (sig1^2 * mue2 - sig2^2 * mue1 - sqrt(-1 * sig1^2 * sig2^2 *...
             (2* mue2 * mue1 - mue2^2 - 2* sig2^2 * log(sig2/sig1) ...
             - mue1^2 + 2* sig1^2*log(sig2/sig1)))) / (-1* sig2^2 + sig1^2);


end
